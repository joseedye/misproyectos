    private void cmdIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdIniciarActionPerformed
        // TODO add your handling code here:
        String usuario = this.txtUsuario.getText();
        String password = this.txtPassword.getText();
        
        if (usuario.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite los datos requeridos !");
            return;
        }
        
        if (usuario.equals("secretaria") && password.equalsIgnoreCase("12345")) {
            InicioSesion inicio = new InicioSesion(this);
            inicio.setVisible(true);
            this.setVisible(false);
        } else { 
            JOptionPane.showMessageDialog(null, "Datos Incorrectos !");
            this.txtUsuario.setText("");
            this.txtPassword.setText("");
        }
    }//GEN-LAST:event_cmdIniciarActionPerformed

