public class Categoria {
    
    private String categoriaY;
    private String categoriaOrange;
    private String categoriaVan;
    private String categoriaCondElegido;
    
    public Categoria(){
        
    }

    public String getCategoriaY() {
        return categoriaY;
    }

    public void setCategoriaY(String categoriaY) {
        this.categoriaY = categoriaY;
    }

    public String getCategoriaOrange() {
        return categoriaOrange;
    }

    public void setCategoriaOrange(String categoriaOrange) {
        this.categoriaOrange = categoriaOrange;
    }

    public String getCategoriaVan() {
        return categoriaVan;
    }

    public void setCategoriaVan(String categoriaVan) {
        this.categoriaVan = categoriaVan;
    }

    public String getCategoriaCondElegido() {
        return categoriaCondElegido;
    }

    public void setCategoriaCondElegido(String categoriaCondElegido) {
        this.categoriaCondElegido = categoriaCondElegido;
    }
    
    
            
            
    
}
