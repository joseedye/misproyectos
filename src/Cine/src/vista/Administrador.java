    public void inicializarComtemp(){
       
        seriesguardadas.removeAllItems();
        if (p.m.getSeries() != null) {
            
            for (Serie bin : p.m.getSeries()) {
                 //System.out.println(bin.getTitulo());
                seriesguardadas.addItem(bin.getTitulo());
            }
        }
      
    }
    
